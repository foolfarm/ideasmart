async function seed() {
  const conn = await mysql.createConnection(DB_URL);
  let inserted = 0;
  let skipped = 0;

  for (const p of personaggi) {
    try {
      const [existing] = await conn.execute("SELECT id FROM personaggi WHERE slug = ?", [p.slug]);
      if (existing.length > 0) {
        console.log(`⏭  Skip (già presente): ${p.name}`);
        skipped++;
        continue;
      }
      await conn.execute(
        `INSERT INTO personaggi 
         (name, slug, role, company, country, bio, full_bio, image_url, personaggio_category, 
          funding_raised, exits, companies_count, linkedin_url, twitter_url, website_url, 
          quote, quote_source, featured, is_italian, view_count, created_at, updated_at)
         VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, 0, NOW(), NOW())`,
        [
          p.name, p.slug, p.role, p.company, p.country, p.bio, p.full_bio, p.image_url,
          p.category, p.funding_raised, p.exits, p.companies_count,
          p.linkedin_url, p.twitter_url, p.website_url,
          p.quote, p.quote_source, p.featured ? 1 : 0, p.is_italian ? 1 : 0,
        ]
      );
      console.log(`✅ Inserito: ${p.name}`);
      inserted++;
    } catch (err) {
      console.error(`❌ Errore per ${p.name}:`, err.message);
    }
  }

  await conn.end();
  console.log(`\nSeed completato: ${inserted} inseriti, ${skipped} saltati`);
}

seed().catch(console.error);
